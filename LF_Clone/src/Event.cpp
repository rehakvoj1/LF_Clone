WindowResizedEvent::WindowResizedEvent( unsigned int width, unsigned int height ) : m_width(width),
																					m_height(height)
{
}

// =================================================================================
unsigned int WindowResizedEvent::GetWidth() const
{
	return m_width;
}

// =================================================================================
unsigned int WindowResizedEvent::GetHeight() const
{
	return m_height;
}

